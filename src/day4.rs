fn parse(input: &str) -> DMatrix<char> {
    input
        .lines()
        .flat_map(str::chars)
        .pipe(|chars| DMatrix::from_row_iterator(140, 140, chars)) // static matrices only implemented up to 128Â² Q.Q
}

pub fn part1() -> usize {    
    let matrix = parse(INPUT);
 
    let rows    = matrix.   row_iter();
    let columns = matrix.column_iter();

    let diag_offsets = matrix
        .map_with_location(|row, col, val| (row as i32 - col as i32, val) )
        .into_iter()
        .cloned() // ?????
        .into_group_map()
        .into_values();

    let anti_diag_offsets = matrix
        .map_with_location(|row, col, val| (row as i32 + col as i32, val) )
        .into_iter()
        .cloned() // ?????
        .into_group_map()
        .into_values();
    
    iter::empty() // just for readability
        .chain(             rows.map(String::from_iter))
        .chain(          columns.map(String::from_iter))
        .chain(     diag_offsets.map(String::from_iter))
        .chain(anti_diag_offsets.map(String::from_iter))
        .map(|s| s.matches("XMAS").count() + s.matches("SAMX").count())
        .sum()
}

pub fn part2() -> i32 {
    let square = INPUT
        .lines()
        .map(str::chars)
        .map(Itertools::collect_vec)
        .collect_vec();

    let mut count = 0;

    for row in 1..139 {
        for col in 1..139 {
            if square[row][col] != 'A' {
                continue;
            }

            let corners = [
                square[row-1][col-1], // top left
                square[row-1][col+1], // top right
                square[row+1][col-1], // bot left
                square[row+1][col+1]  // bot right
            ];

            match String::from_iter(corners).as_str() {
                "MMSS" |
                "SSMM" |
                "MSMS" |
                "SMSM" => { count += 1 },

                _ => {}
            }
        }
    }

    count
}

